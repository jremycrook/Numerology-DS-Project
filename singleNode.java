/**
 * This class sets up the nodes for single linked lists
 */
public class singleNode 
{
	//variable declaration
	public Numerology num;
	public singleNode next;
	
	//constructor
	public singleNode(Numerology numer) 
	{
		num = numer;
		next = null;
		
	}//end constructor
}//end singleNode
